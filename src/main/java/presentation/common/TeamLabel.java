package presentation.common;

import java.awt.Dimension;

public class TeamLabel extends MyLabel{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public TeamLabel(String name, Dimension dimension, int x, int y) {
		super(name, dimension, x, y);
	}
	
	public TeamLabel(String name, int x, int y){
		this(name, new Dimension((int)(120*Admin.proportion), (int)(20*Admin.proportion)),(int)(x*Admin.proportion), (int)(y*Admin.proportion));
	}
}
