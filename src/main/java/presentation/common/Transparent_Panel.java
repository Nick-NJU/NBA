package presentation.common;

import javax.swing.JPanel;

public class Transparent_Panel extends JPanel{

	/**
	 * 透明panel
	 */
	private static final long serialVersionUID = 1L;
	
	protected int width;
	protected int height;
	
	/**
	 * 
	 * @param width 宽
	 * @param height 高
	 */
	public Transparent_Panel(int width, int height) {
		setOpaque(false);
		setBackground(null);
		setLayout(null);
		this.width = width;
		this.height = height;
		setSize(width, height);
	}
}
